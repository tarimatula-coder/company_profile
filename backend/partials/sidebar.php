        <script>
            $(document).ready(function() {
                // Saat menu sidebar diklik
                $("#sidebarnav a.sidebar-link").on("click", function(e) {
                    e.preventDefault(); // Cegah redirect langsung

                    // Ambil href/id dari menu yang diklik
                    var target = $(this).attr("href");
                    console.log("Menu diklik:", target);

                    // Hapus semua active, tambahkan active ke menu yang diklik
                    $("#sidebarnav a.sidebar-link").removeClass("active");
                    $(this).addClass("active");

                    // Auto scroll sidebar agar menu yang diklik berada di tengah
                    var container = $(".scroll-sidebar"); // Sidebar container (yang punya scrollbar)
                    var scrollTo = $(this).position().top + container.scrollTop() - (container.height() / 2);

                    container.animate({
                        scrollTop: scrollTo
                    }, 400);

                    // Pindah halaman setelah animasi selesai (opsional)
                    setTimeout(() => {
                        window.location.href = target;
                    }, 300);
                });
            });
        </script>